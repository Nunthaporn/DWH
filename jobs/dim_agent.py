    print(f"üîç New IDs to insert: {len(new_ids)}")
    print(f"üîç Common IDs to compare: {len(common_ids)}")
    print(f"üîç Data to insert: {len(df_to_insert)}")
    print(f"üîç Common new data: {len(df_common_new)}")
