    df_cleaned = df_cleaned[df_cleaned["card_ins_type"] != "‡∏ç‡∏≤‡∏ï‡πÄ‡∏õ‡πá‡∏ô‡∏ô‡∏≤‡∏¢‡∏´‡∏ô‡πâ‡∏≤"]

